package practice;

public class Prac02 {
	
	public static void main(String[] args) {
		
		Byte b = 126;
		Short s = 31760;
		Integer in = 10;
		Long ong = 2000000000L;
		Float f = 3.2F;
		Double d = 3.45;
		Character c = 'A';
		Boolean boo = true;
		
		System.out.printf("Byte: %d Short: %d Integer: %d%nLong: %d Float: %f Double: %f%nCharacter: %c Boolean: %b", 
							b, s, in, ong, f, d, c, boo);
	}

}
